def make_score_tokens(
    *,
    beatmap_md5: str = "a" * 32,
    username: str = "tester",
    online_checksum: str = "c" * 32,
    n300: int = 100,
    n100: int = 0,
    n50: int = 0,
    ngeki: int = 0,
    nkatu: int = 0,
    nmiss: int = 0,
    score: int = 123456,
    max_combo: int = 100,
    full_combo: str = "True",
    grade: str = "S",
    mods: int = 0,
    passed: str = "True",
    mode: int = 0,
) -> list[str]:
    return [
        beatmap_md5,
        username,
        online_checksum,
        str(n300),
        str(n100),
        str(n50),
        str(ngeki),
        str(nkatu),
        str(nmiss),
        str(score),
        str(max_combo),
        full_combo,
        grade,
        str(mods),
        passed,
        str(mode),
    ]
