def make_score(
    *,
    id: int = 1,
    map_md5: str = "a" * 32,
    user_id: int = 1,
    mode: Mode = Mode.STD,
    mods: Mods = Mods(0),
    pp: float = 0.0,
    sr: float = 0.0,
    score: int = 0,
    max_combo: int = 0,
    acc: float = 0.0,
    n300: int = 0,
    n100: int = 0,
    n50: int = 0,
    nmiss: int = 0,
    ngeki: int = 0,
    nkatu: int = 0,
    passed: bool = False,
    quit: bool = False,
    full_combo: bool = False,
    status: ScoreStatus = ScoreStatus.FAILED,
    time: int = 0,
    time_elapsed: int = 0,
    online_checksum: str | None = None,
) -> Score:
    return Score(
        id=id,
        map_md5=map_md5,
        user_id=user_id,
        mode=mode,
        mods=mods,
        pp=pp,
        sr=sr,
        score=score,
        max_combo=max_combo,
        acc=acc,
        n300=n300,
        n100=n100,
        n50=n50,
        nmiss=nmiss,
        ngeki=ngeki,
        nkatu=nkatu,
        passed=passed,
        quit=quit,
        full_combo=full_combo,
        status=status,
        time=time,
        time_elapsed=time_elapsed,
        online_checksum=online_checksum,
    )


